    private void cargarComboBox(){
        List<TipoTransportista> tiposTransportistas = tipoJpa.findTipoTransportistaEntities();
        for (TipoTransportista p : tiposTransportistas) {
            cbType.addItem(p.getDescripcion());
        }
        
    }

